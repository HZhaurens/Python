# Basic data types in Python

# Integer
x = 10

# Float
y = 3.14

# Boolean
is_true = True
is_false = False

# Complex number
z = 3 + 4j

# None type
empty = None

print(f"Integer: {x}, type: {type(x)}")
print(f"Float: {y}, type: {type(y)}")
print(f"Boolean: {is_true}, type: {type(is_true)}")
print(f"Complex: {z}, type: {type(z)}")
print(f"None: {empty}, type: {type(empty)}")